const DEFAULT_LOCALE: &str = "ko-KR";

pub fn projection_files(
    root: PathBuf,
    scope: InstallScope,
    registry: &TemplateRegistry,
) -> Result<Vec<PlannedFile>> {
    let mut files = Vec::new();
    files.push(PlannedFile::new(
        root.join("AGENTS.md"),
        codex_agents_md(registry)?,
    ));
    files.push(PlannedFile::new(root.join("config.toml"), codex_config()));
    files.push(PlannedFile::new(
        root.join("hooks.json"),
        codex_hooks_json(scope, &root),
    ));

    for workflow in registry.workflows() {
        files.push(PlannedFile::new(
            root.join("skills").join(&workflow.name).join("SKILL.md"),
            workflow.content.clone(),
        ));
    }

    for fragment in registry.fragments() {
        files.push(PlannedFile::new(
            root.join(&fragment.relative_path),
            fragment.content.clone(),
        ));
    }

    for agent in registry.agents() {
        let (agent_id, agent_content) = agent_toml(agent)?;
        files.push(PlannedFile::new(
            root.join("agents").join(format!("{agent_id}.toml")),
            agent_content,
        ));
    }

    Ok(files)
}

fn codex_agents_md(registry: &TemplateRegistry) -> Result<String> {
    let config = HarnessConfig::from_registry(registry)?;
    let workflows = registry
        .workflows()
        .iter()
        .map(|workflow| format!("- `{}`", workflow.name))
        .collect::<Vec<_>>()
        .join("\n");
    let fragments = registry
        .fragments()
        .iter()
        .map(|fragment| format!("- `{}`", fragment.name))
        .collect::<Vec<_>>()
        .join("\n");
    let agents = registry
        .agents()
        .iter()
        .map(|agent| format!("- `{}`", agent.name))
        .collect::<Vec<_>>()
        .join("\n");

    Ok(format!(
        r#"# Megara Codex Harness

This file is generated by Megara. Edit the Megara SSOT, then run `megara sync`.

## Locale

- Locale: `{}`.
- Write user-facing prose in the configured locale unless the user explicitly requests another language.
- The locale rule applies to progress updates, clarification questions, option labels, assumptions, plans, status reports, and final answers.
- Keep code identifiers, file paths, commands, config keys, package names, API names, and quoted source text unchanged.
- Do not mix languages in explanatory prose. If technical literals are English, explain around them in the configured locale.
- Before sending a user-facing response, rewrite stock English workflow phrases into the configured locale. Do not leave English lead-ins such as "Next", "I will", "Done", or "Unit suite is green" in prose when the configured locale is not English.
- Structured Megara block keys must stay parseable, but free-text values such as `question`, `options`, `rationale`, and `summary` should use the configured locale unless they are technical literals.

## Workflows

{workflows}

## Internal Fragments

{fragments}

## Hooks

- Codex hooks call `megara hook` directly.

## Agents

{agents}

## Rules

- Clarify ambiguity before implementation.
- Keep implementation scoped to the accepted plan.
- Verify behavior before final response.
"#,
        config.locale
    ))
}

fn codex_config() -> String {
    r#"# Megara Codex projection.
# Codex discovers skills, agents, and hooks from this directory.
"#
    .to_string()
}

fn codex_hooks_json(scope: InstallScope, root: &Path) -> String {
    let hooks = serde_json::json!({
        "hooks": {
            "UserPromptSubmit": [{
                "hooks": [{
                    "name": "megara-hook-UserPromptSubmit",
                    "type": "command",
                    "command": hook_command(scope, root, "UserPromptSubmit"),
                    "timeout": 21
                }]
            }],
            "PreToolUse": [{
                "hooks": [{
                    "name": "megara-hook-PreToolUse",
                    "type": "command",
                    "command": hook_command(scope, root, "PreToolUse"),
                    "timeout": 10
                }]
            }],
            "Stop": [{
                "hooks": [{
                    "name": "megara-hook-Stop",
                    "type": "command",
                    "command": hook_command(scope, root, "Stop"),
                    "timeout": 10
                }]
            }]
        }
    });
    format!(
        "{}\n",
        serde_json::to_string_pretty(&hooks).expect("hooks json is serializable")
    )
}

fn hook_command(scope: InstallScope, root: &Path, event: &str) -> String {
    let scope = scope.to_string();
    let project_root = match scope.as_str() {
        "project" => root
            .parent()
            .map(|path| {
                format!(
                    " --project-root {}",
                    shell_quote(&path.display().to_string())
                )
            })
            .unwrap_or_default(),
        _ => String::new(),
    };
    format!(
        "megara hook --managed-marker MEGARA:MANAGED --scope {scope}{project_root} --runtime codex --event {event}"
    )
}

fn shell_quote(value: &str) -> String {
    format!("\"{}\"", value.replace('"', "\\\""))
}

