const DEFAULT_LOCALE: &str = "ko-KR";

pub fn projection_files(
    root: PathBuf,
    scope: InstallScope,
    registry: &TemplateRegistry,
) -> Result<Vec<PlannedFile>> {
    let mut files = Vec::new();
    files.push(PlannedFile::new(
        root.join("AGENTS.md"),
        codex_agents_md(registry)?,
    ));
    files.push(PlannedFile::new(root.join("config.toml"), codex_config()));
    files.push(PlannedFile::new(
        root.join("hooks.json"),
        codex_hooks_json(scope, &root),
    ));

    for workflow in registry.workflows() {
        files.push(PlannedFile::new(
            root.join("skills").join(&workflow.name).join("SKILL.md"),
            workflow.content.clone(),
        ));
    }

    for fragment in registry.fragments() {
        files.push(PlannedFile::new(
            root.join(&fragment.relative_path),
            fragment.content.clone(),
        ));
    }

    for agent in registry.agents() {
        let (agent_id, agent_content) = agent_toml(agent)?;
        files.push(PlannedFile::new(
            root.join("agents").join(format!("{agent_id}.toml")),
            agent_content,
        ));
    }

    Ok(files)
}

