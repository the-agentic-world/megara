use std::path::PathBuf;

use crate::{
    installer::PlannedFile,
    templates::{HarnessTemplate, TemplateRegistry},
};

pub fn projection_files(root: PathBuf, registry: &TemplateRegistry) -> Vec<PlannedFile> {
    let mut files = Vec::new();
    files.push(PlannedFile::new(
        root.join("AGENTS.md"),
        codex_agents_md(registry),
    ));
    files.push(PlannedFile::new(root.join("config.toml"), codex_config()));

    for workflow in registry.workflows() {
        files.push(PlannedFile::new(
            root.join("skills").join(workflow.name).join("SKILL.md"),
            skill_content(workflow),
        ));
    }

    for agent in registry.agents() {
        files.push(PlannedFile::new(
            root.join("agents").join(format!("{}.toml", agent.name)),
            agent_toml(agent),
        ));
    }

    files
}

fn codex_agents_md(registry: &TemplateRegistry) -> String {
    let workflows = registry
        .workflows()
        .iter()
        .map(|workflow| format!("- `{}`", workflow.name))
        .collect::<Vec<_>>()
        .join("\n");
    let agents = registry
        .agents()
        .iter()
        .map(|agent| format!("- `{}`", agent.name))
        .collect::<Vec<_>>()
        .join("\n");

    format!(
        r#"# Megara Codex Harness

This file is generated by Megara. Edit the Megara SSOT, then run `megara sync`.

## Workflows

{workflows}

## Agents

{agents}

## Rules

- Clarify ambiguity before implementation.
- Keep implementation scoped to the accepted plan.
- Verify behavior before final response.
"#
    )
}

fn codex_config() -> String {
    r#"# Megara Codex projection.
# Codex discovers skills and agents from this directory.
"#
    .to_string()
}

fn skill_content(template: &HarnessTemplate) -> String {
    format!(
        r#"---
name: {}
description: "Megara workflow: {}"
---

{}
"#,
        template.name, template.name, template.content
    )
}

fn agent_toml(template: &HarnessTemplate) -> String {
    format!(
        r#"name = "{}"
description = "Megara {} role agent"

instructions = """
{}
"""
"#,
        template.name, template.name, template.content
    )
}
